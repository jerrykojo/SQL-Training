Select 
C.CUSTOMER_ID, C.FIRST_NAME, C.LAST_NAME, C.AGE,C.COUNTRY, O.ITEM
From BEST_BUY_CUSTOMERS C
Join
BEST_BUY_ORDERS O
On C.CUSTOMER_ID = O.CUSTOMER_ID
Where O.ITEM = 'Mouse'


--2:SHOW ME THE CUSTOMER INFO AND THE ITEM THAT THEY ORDER, THE PRODUCT BUT ONLY SHOW THE PRODUCT FOR PEN
select 
A.CUSTOMER_ID,
A.FIRST_NAME,
A.LAST_NAME,
A.AGE,
A.COUNTRY,
B.ORDER_ID,
B.ITEM,
B.AMOUNT,
C.PRODUCT_ID,
C.PRODUCT_NAME
FROM BEST_BUY_CUSTOMERS A
LEFT JOIN BEST_BUY_ORDERS B
ON A.CUSTOMER_ID = B.CUSTOMER_ID
LEFT JOIN BEST_BUY_PRODUCTS C
ON A.CUSTOMER_ID = C.CUSTOMER_ID
WHERE C.PRODUCT_NAME= 'PEN'



/**Joins:
1: Inner Join = used to retrieve/RETURN rows where matching values exist in both tables
2: Left Join = retrieve/RETURNS  (RETURN MATCHING RECORDS FROM RIGHT TO LEFT) 
all rows from the left table and matching rows from the right table
3: Right Join = retrieve/RETURNS (RETURN MATCHING RECORDS FROM LEFT TO LEFT)
all rows from the right table and matching rows from the left table 
(THIS IS THE OPPOSITE OF LEFT JOIN)
4: Full Join = combine the results of both LEFT JOIN and RIGHT JOIN
**/




--USING INNER JOIN COMMAND
SELECT 
